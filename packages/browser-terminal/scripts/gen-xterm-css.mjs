// Embeds @xterm/xterm's stylesheet as a TS string so the panel can inject
// it into its Shadow DOM root (page styles don't pierce shadow boundaries,
// and consumers shouldn't need a CSS import or bundler support).
import { createRequire } from 'node:module';
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const require = createRequire(import.meta.url);
const cssPath = require.resolve('@xterm/xterm/css/xterm.css');
const css = readFileSync(cssPath, 'utf8');

const outDir = join(dirname(fileURLToPath(import.meta.url)), '..', 'src', 'generated');
mkdirSync(outDir, { recursive: true });
writeFileSync(
  join(outDir, 'xterm-css.ts'),
  `// Generated by scripts/gen-xterm-css.mjs — do not edit.\nexport const XTERM_CSS: string = ${JSON.stringify(css)};\n`,
);
console.log('generated src/generated/xterm-css.ts');
